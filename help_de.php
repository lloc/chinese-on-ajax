            <h2>Hilfe</h2>
            <p>
                Die Handhabung ist bewu&szlig;t recht einfach gehalten. In der
                oberen Box soll zum Erlernen der chinesischen Ideogramme, deren
                <i>pinyin</i>-Umschreibung in das Texteingabefeld eingetragen
                werden. Dabei ist es m&ouml;glich, komplett ohne die Buttons
                auszukommen, die mit den verschieden T&ouml;nen beschriftet
                wurden. Es ist lediglich etwas Wissen &uuml;ber Notation in
                <i>pinyin</i> n&ouml;tig, wie sie beispielsweise beim
                <a rel="nofollow" title="Externer Link: Mandarintools" href="http://www.mandarintools.com/cedict.html">CEDICT</a>
                verwendet wird. Allerdings gibt es einige kleine Unterschiede,
                die die Verwendung vereinfachen sollen oder dem Umstand Rechnung
                tragen, da&szlig; die chinesische Regierung die
                Zusammenschreibung von Begriffen propagiert. So wird man in
                vielen Online-W&ouml;rterb&uuml;chern Eintr&auml;ge wie z.B.
                <strong>zhong1 guo2 ren2</strong> 中国人 finden. Hier 
                m&uuml;ssen die Silben dieses Begriffs zusammengeschrieben
                werden (auch wenn die Umformung trotzdem funktioniert), weil die
                Kontrolle der Eingaben auf meinen Eintr&auml;gen basiert.
            </p>
            <p>
                Nach der Eingabe des <i>pinyin</i> und der Best&auml;tigung
                durch die [RETURN]-Taste wird die Zeichenkette automatisch
                umgeformt. Das obige Beispiel w&uuml;rde zu
                <strong>zhōngguórén</strong>. Der neutrale Ton eines Vokals
                mu&szlig; <strong>nicht</strong> zwingend angegeben werden. Es
                ist aber trotzdem m&ouml;glich, selbigen durch die Zahlen 0 oder
                5 anzuzeigen, weil einige bekanntere W&ouml;rterb&uuml;cher dies
                auch so handhaben. <strong>Ni3 hao3 ma?</strong> ist
                ausreichend, aber auch die Eingaben
                <strong>Ni3 hao3 ma5?</strong> oder
                <strong>Ni3 hao3 ma0?</strong> ergeben nach der Best&auml;tigung
                <strong>Nǐ hǎo ma?</strong>. Der Umlaut <strong>ü</strong> kann
                auch mit dem Buchstaben <strong>v</strong>, der im <i>pinyin</i>
                keine Verwendung findet, notiert werden. Bei
                &Uuml;bereinstimmung von Schriftzeichen und <i>pinyin</i> wird
                der Erfolg durch Umf&auml;rbung der Box mit gr&uuml;nen (bzw.
                Mi&szlig;erfolg bei falscher Eingabe roten) Farbt&ouml;nen
                angezeigt. Mit der [TAB]-Taste wird die n&auml;chste Abfrage
                gestartet. Mittels [ESC] kann man den Lernmodus wechseln, um
                entweder nur einzelne W&ouml;rter oder ganze S&auml;tze zu
                &uuml;ben.
            </p>
            <p>
                Es werden &uuml;brigens nur
                die vereinfachten Ideogramme angezeigt, die im Alltag auch
                verwendet werden. Die komplizierte Form findet man eher in der
                <a rel="nofollow" title="Externer Link: China-Phoenix" href="http://www.china-phoenix.net/chinese_calligraphy/calligraphy.html">chinesischen Kalligrafie</a>.
            </p>
