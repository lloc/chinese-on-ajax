            <h2>Premessa</h2>
            <p>
                Non penso che esista <strong>un</strong> metedo di apprendimento
                valido per tutti. A me personalmente, ad esempio, risulta
                difficile trovare uno stimolo per memorizzare vocaboli nelle
                poche ore libere a mia disposizione. D'altra parte però mi
                rilasso risolvendo problemi informatici (oltre che col
                <a title="Go 2 Bowling-Liga.it" href="http://www.bowling-liga.it/">Bowling</a>
                o con un
                <a title="Go 2 Erborista.biz" href="http://www.erborista.biz/">massaggio rilassante</a>
                dallo zio
                <a title="Go 2 Egiabenessere.it" href="http://www.egiabenessere.it/">Claudio</a>).
                Ecco che è nata l'idea di trasferire il
                <a title="Contenuto del corso di cinese" href="list.php?lang=it">contenuto del corso di cinese</a> -
                che frequento da ottobre 2007 - in una struttura di programma
                che renda possibile migliorare la mia conoscenza dei vocaboli.
                Per utilizzare l'applicazione è necessario aver abilitato
                Javascript, che dovrebbe comunque essere standard nella maggior
                parte dei casi. Potete trovare una sorta di guida in fondo al
                testo. I riquadri <strong>Premessa</strong> e
                <strong>Aiuto</strong> possono essere chiusi cliccando sul
                bottone nell'angolo in alto a destra, in modo da poter lavorare
                indisturbati.
            </p>
            <p>
                L'applicazione si serve del
                <a rel="nofollow" title="Go 2 Prototypejs.org" href="http://www.prototypejs.org/">Prototype-Framework</a>,
