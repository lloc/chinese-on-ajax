            <p>
                Ich glaube nicht daran, da&szlig; es <strong>die</strong> eine
                Lernmethode f&uuml;r alle gibt. Mir pers&ouml;nlich f&auml;llt
                es beispielsweise schwer, mich in den wenigen freien Stunden,
