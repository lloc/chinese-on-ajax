                <a title="Externer Link: Sizilienhotels.de" href="http://sizilienhotels.de/">Urlaub in Sizilien</a>)
                zu entspannen. So ist die Idee entstanden, den
                <a title="Lernstoff des Chinesisch-Kurses" href="list.php">Lernstoff des Chinesisch-Kurses</a> -
                an dem ich teilnehme - in eine Programmstruktur zu
                &uuml;berf&uuml;hren und auf diese Art und Weise meine
                Vokabelkenntnisse zu verbessern. Um die Anwendung nutzen
