            <h2>Aiuto</h2>
            <p>
                L'uso del programma è molto semplice: in alto vengono
                visualizzati gli ideogrammi cinesi e nel riquadro appena
                sottostante si inserisce la trascrizione in <i>pinyin</i>.
                &Egrave; necessaria solo una minima conoscenza sulla notazione
                del <i>pinyin</i> come viene utilizzata ad esempio in
                <a rel="nofollow" title="Go 2 Mandarintools.com" href="http://www.mandarintools.com/cedict.html">CEDICT</a>
                e che verrà spiegata qui di seguito. Tuttavia ci sono un paio di
                piccole differenze che ne facilitano l'uso o che tengono conto
                della tendenza più comune di scrivere unite le varie parti che
                compongono un vocabolo (ad es. zhongguórén e non zhong guó rén
                ). Come indicare i toni? Si può scegliere di cliccare sui
                bottoni su cui sono rappresentate le vocali con i diversi toni
                oppure ricorrere ad un altro metodo comune che è quello di
                indicare alla fine di ogni sillaba il numero corrispondente al
                tono. Ad esempio, guìxìng sarà gui4xing4: confermando con il
                tasto ENTER si visualizzerà automaticamente la trascrizione
                corretta (guìxìng). E ancora: hao sarà hao3, nin sarà nin2,
                zhongguórén sarà zhong1guo2ren2 e così via.
            </p>
            <p>
                Il tono neutro di una vocale <strong>non</strong> va
                necessariamente indicato. &Egrave; tuttavia possibile farlo
                digitando il numero 0 o il 5, come in alcuni tra i dizionari più
                conosciuti. <strong>Ni3 hao3 ma?</strong> è sufficiente, ma
                anche <strong>Ni3 hao3 ma5?</strong> così come <strong>Ni3 hao3
                ma0?</strong> dopo la conferma compaiono in automatico come
                <strong>Nǐ hǎo ma?</strong>. Il suono <strong>ü</strong> può
                essere indicato anche con la lettera <strong>v</strong> che in
                <i>pinyin</i> non trova altro uso. Se viene indicata la
                trascrizione esatta dei caratteri il riquadro si colora di verde
                (e di rosso in caso di errore). Con il tasto [TAB] si richiama
                il carattere successivo. Con [ESC] si può modificare la modalità
                di esercizio, potendo scegliere tra singole parole o frasi
                intere.
            </p>
            <p>
                Gli ideogrammi sono indicati secondo la trascrizione del cinese
                semplificato comunemente usata. Per la trascrizione complicata
                si rimanda alla
                <a rel="nofollow" title="Go 2 China-Phoenix.net" href="http://www.china-phoenix.net/chinese_calligraphy/calligraphy.html">calligrafia cinese</a>.
            </p>
